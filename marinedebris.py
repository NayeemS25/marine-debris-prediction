from ultralytics import YOLO

# Load YOLOv8 model
model = YOLO("yolov8s.pt")   # you can use yolov8n.pt (nano), yolov8m.pt (medium), etc.

# Train on your Roboflow dataset
model.train(
    data="/content/datasets/data.yaml",  # path to Roboflow's data.yaml
    epochs=50,                           # number of training epochs
    imgsz=640,                           # image size
    batch=16,                            # adjust depending on GPU memory
    name="roboflow_yolov8"               # experiment name
)

from ultralytics import YOLO

# Load the best trained model
model = YOLO("runs/detect/roboflow_yolov8/weights/best.pt")

# Run prediction on validation set
